#[cfg(test)]
mod tests {
    use super::*;
    use std::str::FromStr;

    #[test]
    fn test_country_code_name() {
        let cc = CountryCode::TT;

        assert_eq!(cc.name(), "Trinidad and Tobago");
    }

    #[test]
    fn test_country_code_code() {
        let cc = CountryCode::TT;

        assert_eq!(cc.code(), "TT");
    }

    #[test]
    fn test_country_code_from_str() {
        let cc1 = CountryCode::from_str("TT");
        let cc2: Result<CountryCode, CountryCodeError> = "TT".parse();

        assert_eq!(cc1, Ok(CountryCode::TT));
        assert_eq!(cc2, Ok(CountryCode::TT));
    }
}
